    <h4 align="center">ข้อปฏิบัติสำหรับการลงทะเบียนสมัครเป็นตัวแทนจำหน่ายสินค้าทุกประเภท</h4>
    <h5 align="center">ของ Alive Dropship</h5>
    <p style="text-align: center">ในเว็บไซต์ ระบบ <span style="font-size: 20px">Alive Dropship</span><br/>
        อันเป็นการทำสัญญาและธุรกรรมทางอิเล็กทรอนิกส์<br/>
        ตามกฎหมายว่าด้วยธุรกรรมทางอิเล็กทรอนิกส์<br/><br/></p>
    <h5>ส่วนของผู้สมัคร (ผู้ตกลงทำสัญญา) </h5>
    <h5>ให้มีข้อมูลดังต่อไปนี้ในระบบ</h5>
    <p style="font-size: 16px">
